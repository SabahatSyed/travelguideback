class User{
    constructor(Status,city,contact,email,gender,profileImage,role,userName){
        this.Status=Status;
        this.city=city;
        this.contact=contact;
        this.email=email;
        this.gender=gender;
        this.profileImage=profileImage;
        this.role=role;
        this.userName=userName;
    }
}
module.exports=User;